#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int		i;
	char	*ptr;

	ptr = (char *)s;
	i = 0;
	while ((size_t)i < n)
	{
		ptr[i] = '\0';
		i++;
	}
}
