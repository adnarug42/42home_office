#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	if (!lst)
		return (NULL);
	while (lst->next)
	{
		if (!(lst->next->next))
			return (lst->next);
		lst = lst->next;
	}
	return (lst);
}
